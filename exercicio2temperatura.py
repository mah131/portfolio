print("Medição de temperatura em Celsius")
#Peça uma temperatura em Celsius ao usuário
temperatura = int(input("Digite uma temperatura em Celsius: "))
# converta para Fahrenhenit utilizando a fórmula: Fahrenheit = (Celsius * 9/5) + 32
Fahrenhenit = temperatura * 9/5 + 32
print("Em Fahrenheit a temperatura é",Fahrenhenit)
print("Obrigado por usar nosso sistema de medição")