print("Calculo de área de um retângulo")
#Solicita os dados para o calculo
print("Digite o valor da largura e altura:")
largura = float(input("Digite a largura do retângulo (em metros):"))
altura = float(input("Digite a altura do retângulo (em metros):"))

#Calcula a área
area = largura + altura

# Exibe o resultado
print(f"A área do retâgulo é {area} metros quadrados.")